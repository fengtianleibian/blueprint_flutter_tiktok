/// 应用中的尺寸统一管理类
///
/// 定义了常用组件的标准尺寸，使用静态属性提供值。
/// 通过集中管理，可以在项目中保持一致的尺寸风格，方便维护和扩展。
class AppSize {
  /// 图标的标准尺寸
  ///
  /// 为图标组件（如 `Icon`）提供统一的尺寸值，默认为 `18`。
  /// 适用于小型图标或导航栏图标。
  static double get icon => 18;

  /// 头像的标准尺寸
  ///
  /// 为头像组件（如 `CircleAvatar`）提供统一的尺寸值，默认为 `24`。
  /// 适合较小的头像布局，如用户列表或评论区域。
  static double get avatar => 24;

  /// 加载指示器的标准尺寸
  ///
  /// 为旋转加载圆圈（如 `CircularProgressIndicator`）提供统一的尺寸值，默认为 `28`。
  /// 适用于加载动画的常见场景。
  static double get indicator => 28;
}
